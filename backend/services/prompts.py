def get_system_prompt(module: str) -> str:
    """获取指定模块的系统提示词"""
    return SYSTEM_PROMPTS.get(module, "")


def get_user_prompt_template(template_name: str) -> str:
    """获取用户提示词模板"""
    return USER_PROMPT_TEMPLATES.get(template_name, "")


def format_user_prompt(template_name: str, **kwargs) -> str:
    """格式化用户提示词模板"""
    template = get_user_prompt_template(template_name)
    try:
        return template.format(**kwargs)
    except KeyError as e:
        raise ValueError(f"提示词模板缺少参数: {e}")
